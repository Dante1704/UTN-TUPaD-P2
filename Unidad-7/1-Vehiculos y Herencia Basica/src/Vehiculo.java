public class Vehiculo {
    private String marca;
    private String modelo;

    public Vehiculo(String marca, String modelo) {
        this.marca = marca;
        this.modelo = modelo;
    }
    
    public String mostrarInfo() {
        return "marca: " + this.marca + ", modelo: " + this.modelo;
    }
  
}
